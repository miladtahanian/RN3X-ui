const translations = { fa, en, zh };

export function t(key, locale = 'fa') {
  const keys = key.split('.');
  let value = translations[locale];
  for (const k of keys) {
    if (!value) return key;
    value = value[k];
  }
  return value ?? key;
}

export function tWithVars(key, vars, locale = 'fa') {
  let text = t(key, locale);
  if (!vars) return text;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(new RegExp(`{{${k}}}`, 'g'), v);
  }
  return text;
}

